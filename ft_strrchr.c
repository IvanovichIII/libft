#include <stddef.h>

char	*ft_strrchr(const char *str, int c)
{
	while (*str)
	{
		str++;
	}
	str--;
	while (*str)
	{
		if (*str == c)
		{
			str--;
			return ((char *)str);
		}
		str--;
	}
	return (NULL);
}
